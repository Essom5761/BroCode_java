public class Main {
    public static void main(String[] args) {
        //println needed for new line compared to print on the same line
        System.out.println("Hello world!");
        System.out.println("Pizza");

        //can also use \n
        System.out.print("I like pizza");
        System.out.print("It's good \n");

        // \t adds a tab
        System.out.println("\t this is a tab");
        // to add quotes in we need a backslash
        System.out.println("\" This is a quote \"");

        /*
        these are comments
         */

        //for System.Out.Println we can just type sout + tab
    }
}